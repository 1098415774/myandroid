package com.example.myapplication.solution;

public class SmartAction {

    private EquipAction action;

    private String smartid;

    private String smartname;

    private int iconid;

    public EquipAction getAction() {
        return action;
    }

    public void setAction(EquipAction action) {
        this.action = action;
    }

    public String getSmartid() {
        return smartid;
    }

    public void setSmartid(String smartid) {
        this.smartid = smartid;
    }

    public String getSmartname() {
        return smartname;
    }

    public void setSmartname(String smartname) {
        this.smartname = smartname;
    }

    public int getIconid() {
        return iconid;
    }

    public void setIconid(int iconid) {
        this.iconid = iconid;
    }
}
